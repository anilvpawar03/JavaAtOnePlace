package task2;

public class OperatorTask2 {

	public static void main(String[] args) {
    
		
		int a = 20;
		int b = 30;
		int c = 40;
		int d = 50;
		
		//Demonstrate Post Increment Operator (a++)  
		
		
		System.out.println(a++);
		System.out.println(a);
		
		System.out.println();
		//Demonstrate Pre Increment Operator (++b) 
		
		System.out.println("Actual b is :"+b);
		System.out.println(++b);
		System.out.println(b);
		

		System.out.println();
     	//Demonstrate Pre Decrement Operator (--c) 
		 
		System.out.println("Actual c is :"+c);
		System.out.println(--c);
		System.out.println(c);
		
		System.out.println();
		//Demonstrate Post Decrement Value (d--)
		
	
		System.out.println(d--);
		System.out.println(d);

		

		
	}

}
