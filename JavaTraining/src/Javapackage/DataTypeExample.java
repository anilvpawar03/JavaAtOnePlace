package Javapackage;

public class DataTypeExample {

	public static void main(String[] args) {
			
		
		//Below datatypes are used to store numeric values 
		
		byte b = 1;
		short s = 2;
		int a = 100;
		
		//Below Datatypes are used to store decimal values 
		float num = 99.99f;
		double num2 =1000.33;
		
		//Below datatypes are use to store boolean value 
		boolean bn = true;
		
		//Below datatypes are used to store char values 
		char ch = 'A';
		
		
		

		System.out.println(b);
		System.out.println(s);
		System.out.println(a);
		System.out.println(num);
		System.out.println(num2);
		System.out.println(bn);
		System.out.println(ch);
		
	
	}

}
