package Javapackage;

public class VariableTypes {
	
		//Global Variable 
	int a = 500;
	
	
		//Static Variable
	static int b = 200;
	
	
	

	public static void main(String[] args) {

		
		//Local Variable 
		int num = 100;
		
		
		
		VariableTypes obj = new VariableTypes();
		System.out.println(obj.a);
		
		
	}

}
