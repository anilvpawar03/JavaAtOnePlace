package operatorprograms;

public class ArithmaticExample {

	public static void main(String[] args) {

		
		int a = 10;
		int b = 2;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		
		// % (Mod) is used to get a remainder 
		System.out.println(a%b);
		
	}

}
