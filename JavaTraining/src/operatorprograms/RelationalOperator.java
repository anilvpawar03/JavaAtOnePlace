package operatorprograms;

public class RelationalOperator {

	//Relational Operator Program
	
	public static void main(String[] args) {

		int a = 300;
		int b = 200;
		
		
		System.out.println(a > b); //true
		System.out.println(a < b); //false
		System.out.println(a == b); //false
		System.out.println(a <= b); //false
		System.out.println(a >= b); //true
		System.out.println(a != b); //true
		
		
	}

}
